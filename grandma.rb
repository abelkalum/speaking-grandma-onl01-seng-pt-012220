def speak-to-grandma
  
  if speak-to-grandma == 'LOVE YOU GRANDMA!'
    returns 'I LOVE YOU TOO PUMPKIN!'

  elsif speak-to-grandma == 'Hi Nana, how are you?'
    returns 'HUH?! SPEAK UP, SONNY!'

  elsif speak-to-grandma == 'Hi!'
    returns 'HUH?! SPEAK UP, SONNY!'

  elsif speak-to-grandma == 'WHAT DID YOU EAT TODAY?'
    returns 'NO, NOT SINCE 1938!'

  else speak-to-grandma == 'WHAT?'
    returns 'NO, NOT SINCE 1938!'
  end
end